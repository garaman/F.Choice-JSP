package kr.co.FChoice.vo;

public class FileVO {
	private int fcf_seq;
	private int fcf_parent;
	private String fcf_path;
	private String fcf_oldName;
	private String fcf_newName;
	private int fcf_download;
	
	public int getFcf_seq() {
		return fcf_seq;
	}
	public void setFcf_seq(int fcf_seq) {
		this.fcf_seq = fcf_seq;
	}
	public int getFcf_parent() {
		return fcf_parent;
	}
	public void setFcf_parent(int fcf_parent) {
		this.fcf_parent = fcf_parent;
	}
	public String getFcf_path() {
		return fcf_path;
	}
	public void setFcf_path(String fcf_path) {
		this.fcf_path = fcf_path;
	}
	public String getFcf_oldName() {
		return fcf_oldName;
	}
	public void setFcf_oldName(String fcf_oldName) {
		this.fcf_oldName = fcf_oldName;
	}
	public String getFcf_newName() {
		return fcf_newName;
	}
	public void setFcf_newName(String fcf_newName) {
		this.fcf_newName = fcf_newName;
	}
	public int getFcf_download() {
		return fcf_download;
	}
	public void setFcf_download(int fcf_download) {
		this.fcf_download = fcf_download;
	}
	
	
}
